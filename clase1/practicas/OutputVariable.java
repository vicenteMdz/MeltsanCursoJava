public class OutputVariable {
    
    public static void main(String[] args) {

	//variable entera value de tipo de dato primitivo e inicializado en 10
        int value = 10;
     
	//variable x de tipo primitivo char e inicializada con A
        char x;
        x = 'A';

	//variable grade de tipo de dato primitivo double 
        double grade = 11;
        
	//muestra en pantalla la variable value
        System.out.println( value );

	//imprime en pantalla la variable x
        System.out.println( "The value of x=" + x );
	
	//imprime la variable grade en pantalla
        System.out.println( "The value of grade =" + grade );
    }
    
}
